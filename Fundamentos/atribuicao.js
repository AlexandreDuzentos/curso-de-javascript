const a = 7
let b = 3;

b += a // b = b + a atribuição aditiva b = 10
console.log(b)

b -= 4 // b = b - 4 atribuicao subtrativa  b = 6
console.log(b)

b *= 2 // b = b * 2 atribuicao multiplicativa b = 12
console.log(b)

b /= 2 // b = b / 2 atribuicao divisiva b = 6
console.log(b)

b %= 2 // b = b % 2 atribuicao de calculo do resto da divisão b = 0
console.log(b)